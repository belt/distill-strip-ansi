// Generated by: python3 (see table generation in project history).
// Entry encoding: bits [4:1] = next_state, bit [0] = action (0=Emit, 1=Skip).
// 15 states × 256 bytes = 3840 bytes. Fits in L1 cache.
include!("state_table.rs");

/// Maps table index back to State enum — avoids unsafe transmute.
#[rustfmt::skip]
static STATE_FROM_INDEX: [State; 15] = [
    State::Ground, State::EscapeStart, State::CsiParam, State::CsiIntermediate,
    State::OscString, State::OscStEsc, State::DcsEntry, State::DcsParam,
    State::DcsPassthrough, State::DcsStEsc, State::StringPassthrough,
    State::StringStEsc, State::Ss2, State::Ss3, State::EscIntermediate,
];
